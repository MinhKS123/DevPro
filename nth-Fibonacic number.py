def fibonacci(n):
    f = [0, 1]
    for i in range(2, n+1):
        f.append(f[i-1] + f[i-2])
    return f[n]


series_check = [int(i) for i in input().split(" ")]
n = int(input())
if fibonacci(n) in series_check:
    print(series_check.index(fibonacci(n))+1)
else:
    print(-1)