def findMaxAverage(arr, n, k):
    if (k > n):
        return -1
    sum = arr[0]
     
    for i in range(1, k):
        sum += arr[i]
 
    max_sum = sum
    max_end = k - 1
    for i in range(k, n):
     
        sum = sum + arr[i] - arr[i - k]
         
        if (sum > max_sum):
         
            max_sum = sum
            max_end = i
    return max_end - k + 1
count = 0
avg = 0
n, k, h = input().split(" ")
int_n = int(n)
int_k = int(k)
int_h = int(h)
list1 = [int(_) for _ in input().split(" ")]
for __ in range(findMaxAverage(list1, int_n, int_k), int_k+1):
    count += list1[__]
    avg += 1
if int_h == 0:
    print(count//avg)
if int_h == 1:
    print(count/avg)