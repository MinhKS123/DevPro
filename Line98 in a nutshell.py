from collections import Counter
import itertools as ite

m, n, k = [int(i) for i in input().split()]
list1 = []
list_res = []
res = 0
a = 0
for _ in range(m):
    list1.append([int(bruh) for bruh in input().split()])
    flatten_list1 = list(ite.chain.from_iterable(list1))
    cnt = Counter(flatten_list1)
for letter in cnt:
    list_res.append(cnt[letter])
    list_res.sort(reverse = True)
for __ in range(k):
    res += list_res[__]
print(res)