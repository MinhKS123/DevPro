from fractions import Fraction

def fraction(a, b):
    list1 = []
    if b == 0:
        return "INVALID"
    res = str(Fraction(a, b))
    for _ in range(len(res)):
        list1.append(res[_])
    list1[2] = ' '
    return list1
str_a, str_b = input().split()
a = int(str_a)
b = int(str_b)
for x in range(len(fraction(a, b))):
    print(fraction(a, b)[x], end='')

